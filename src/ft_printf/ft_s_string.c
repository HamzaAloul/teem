#include "ft_printf.h"

size_t	ft_s_string(const char *s)
{
	size_t	i;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = 0;
	while (s[i])
		i++;
	write(1, s, i);
	return (i);
}
